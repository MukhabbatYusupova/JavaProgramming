package day03_helloworld_variables;

public class MyVariables {
	public static void main(String[] args) {
		int n; // declare the variable and i can store whole numbers
		n = 100; // assigning 100 to the n container no need ""
		
		System.out.println(n);
		
		int a; // only in Java u have to declare, other languages a = 3456 is fine 
		a = 3456;
		
		System.out.println(a);
		
		a = 1000; // u dont declare again variable a
		 
		System.out.println(a);
		
		
	}

}
