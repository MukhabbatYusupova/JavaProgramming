package day03_helloworld_variables;

public class VariableDeclarations {
	public static void main(String[] args) {
		//Declare single variable
		int apples;
		apples = 14;
		//Declare multiple variables in single statement
		int bananas = 10, oranges = 2, mango = 11;
		
		System.out.println (apples);
		
		
		
		// declare variable and assign value in single statement
		int kiwi = 14;
		
		System.out.println(kiwi); // no "", if u write "kiwi" it prints kiwi not the number
		
		// declare and assign value to multiple variables
		//in single statement
		
		int watermelon = 11, grapes = 100, cherries =12, apricots =10; 
		System.out.print(watermelon ); 
		System.out.print(grapes );
		System.out.print(cherries );
		System.out.print(apricots);
		
	}

}
