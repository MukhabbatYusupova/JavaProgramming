package day03_helloworld_variables;

public class PrintlnVSPrint {
	public static void main(String[] args) {
		System.out.println("A"); //prints only this line seperately
		System.out.println("B");
		System.out.print("C "); //prints C and D at the same line
		System.out.println("D"); //use ln if u don want other words come after D at the same line
		
	    System.out.print("Apple ");
	    System.out.print("Banana ");
	    System.out.print("Orange ");
	    System.out.println("Mango"); // fruits till mango will come in the same line
	    System.out.print("Strawberry"); // u have to put space after strawberry to print them not attached
	    System.out.print("Raspberry");
	}

}
