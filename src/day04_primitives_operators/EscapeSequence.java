package day04_primitives_operators;

public class EscapeSequence {
	public static void main(String [] args) {
		
		System.out.println ("He said \"Hello\"."); //if we need "" use \ before every quote
		
		System.out.println("Would you like \'coffe\' or \'tea\'?");
		
		System.out.println("\"\\__/\"");
		
		System.out.println("Java\nSelenium");
		
		// i wanna print out: "I want an 'Ice-cream'", - he said."
		
		System.out.println("\"I want an \'Ice-cream\',-he said.\"");
		
		//I wanna print: -Hi,; -Hi, How are you? -I am good, thanks. (all different lines)
		
		System.out.println("-Hi;\n-Hi, How are you?\n-I am good, thanks.");
		
		// i wanna try something with tab and new line \n\t
		System.out.println("Live\n\tLove\n\t\tLike");
		
		
		                 
	}

}
