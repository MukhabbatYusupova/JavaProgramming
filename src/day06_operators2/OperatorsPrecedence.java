package day06_operators2;

public class OperatorsPrecedence {
	
	public static void main(String[]args) {
		
	System.out.println("hello "+ 6+10); // we get hello 610
	System.out.println(6+10 + " hello");// we get 16 hello
	System.out.println("hello "+ (6+10));// we get hello 16
		
	}

}
